def get_prompts(market: str = "TW") -> tuple[str, str]:
    """依市場回傳 (分類 system prompt, 歸併 prompt)。"""
    if str(market).upper() == "US":
        return THEME_SYSTEM_PROMPT_US, THEME_MERGE_PROMPT_US
    return THEME_SYSTEM_PROMPT_TW, THEME_MERGE_PROMPT_TW


